#include <stdio.h>

int main() {

    //varibale declarations
    int rsk_a = 5;
    int rsk_b = 10;


    printf("\n\n");

    printf("A = %d\n", rsk_a);
    printf("A = %d\n", rsk_a++);
    printf("A = %d\n", rsk_a);
    printf("A = %d\n", ++rsk_a);

    printf("B = %d\n", rsk_b);
    printf("B = %d\n", rsk_b--);
    printf("B = %d\n", rsk_b);
    printf("B = %d\n", --rsk_b);
    
    printf("\n\n");
    
    return (0);

}